print(emoji.demojize('–≠—Ç–æ üêà') + '\n')

"""
–ü–æ —É–º–æ–ª—á–∞–Ω–∏—é –¥–ª—è –∫–æ–¥–æ–≤ –≤—ã–±—Ä–∞–Ω –∞–Ω–≥–ª–∏–π—Å–∫–∏–π —è–∑—ã–∫, –¥—Ä—É–≥–æ–π –Ω–∞–º –Ω–µ –ø–æ–Ω–∞–¥–æ–±–∏—Ç—Å—è. –ï—Å—Ç—å —Ç–∞–∫–∂–µ —É–ø—Ä–æ—â–µ–Ω–Ω—ã–π '—è–∑—ã–∫', alias.
"""
